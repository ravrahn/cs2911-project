/**
 * Coordinate Interface
 *
 * @author AwolGerbil
 */

public interface Coordinate {
    /**
     * Returns the x value
     * 
     * @return x
     */
    public int getX();
    
    /**
     * Sets the x value
     * 
     * @param x
     *            The x value
     */
    public void setX(int x);
    
    /**
     * Returns the y value
     * 
     * @return y
     */
    public int getY();
    
    /**
     * Sets the y value
     * 
     * @param y
     *            The y value
     */
    public void setY(int y);
}
